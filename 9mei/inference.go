package main

import "fmt"

func main() {
    name := "John Doe" // tipe data variabel name akan ditentukan secara otomatis
    age := 30          // tipe data variabel age akan ditentukan secara otomatis
    salary := 5000.0   // tipe data variabel salary akan ditentukan secara otomatis

    fmt.Printf("Name: %s\n", name)
    fmt.Printf("Age: %d\n", age)
    fmt.Printf("Salary: %f\n", salary)
}
